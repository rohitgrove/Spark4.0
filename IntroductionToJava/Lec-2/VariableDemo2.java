public class VariableDemo2 {
    public static void main(String[] args) {
        // byte tyes
        byte age = 25;
        short year = 2025;
        int sal = 10000;
        long req = 98979979789L;

        // floating
        float payment = 1000.65f;
        double bigPay = 1000.59828;

        // character grade
        char grade = 'A';

        // boolean
        boolean flag = true;

        System.out.println("Age  :" + age);
        System.out.println("year :" + year);
        System.out.println("sal  :" + sal);
        System.out.println("req  :" + req);
        System.out.println("pay  :" + payment);
        System.out.println("bP   :" + bigPay);
        System.out.println("grade  :" + grade);
        System.out.println("flage  :" + flag);
    }
}